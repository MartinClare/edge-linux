function getByPath(obj: Dict, path: string): string | Dict | undefined {
  return path.split('.').reduce<string | Dict | undefined>((acc, key) => {
    if (!acc || typeof acc === 'string') return undefined;
    return acc[key];
  }, obj);
}

export function translate(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const raw = getByPath(messages[locale], key) ?? getByPath(messages.en, key);
  if (typeof raw !== 'string') return key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ''));
}
